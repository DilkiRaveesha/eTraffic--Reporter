void SaveUser() {
  
    try{
     Connection con = DBConnection.CreateConnection();
        String sql = "INSERT INTO users(name, nic, address, mobile_number, email, password) "
                + "VALUES (?, ?, ?, ?, ?, ?)";

        PreparedStatement stmt = con.prepareStatement(sql);
        stmt.setString(1, txt_name.getText());
        stmt.setString(2, txt_NIC.getText());
        stmt.setString(3, txt_address.getText());
        stmt.setString(4, txt_MobileNo.getText());
        stmt.setString(5, txt_email.getText());
        stmt.setString(6, txt_password.getText()); // 

        int result = stmt.executeUpdate();

            if (result > 0) {
            JOptionPane.showMessageDialog(this, "User has been registered!", "Success", JOptionPane.INFORMATION_MESSAGE);
            LogInPage loginPage = new LogInPage();
    loginPage.setVisible(true);

    // Close the current RegisterWindow
    this.dispose(); // or setVisible(false);
        }
     
   }catch(Exception e){
     JOptionPane.showMessageDialog(null, "âŒ JDBC Driver not found:\n" + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
   }
     


      

       

   
}



