package Main;

/**
 *
 * @author Dilki Raveesha
 */
public class ComboItem {
        private String key;   // Display name
    private String value; // Actual value or code

    public ComboItem(String key, String value) {
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public String getValue() {
        return value;
    }

    @Override
    public String toString() {
        return key;  // Show this in the combo box
    }
}
